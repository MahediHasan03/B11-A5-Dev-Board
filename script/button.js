

document.getElementById("button1").addEventListener("click", 
    function(){
        alert("Board Update Successfully");
        const container =document.getElementById("activity-box")
        const p =document.createElement("p");
        const btn1 = document.getElementById("button1Text").innerText;
        let currentTime = new Date().toLocaleTimeString();
        p.innerText=`
        You have Complete The Task ${btn1} at ${currentTime};
        `

        // p tag style
        p.style.display="flex";
        p.style.alignItems="center";
        p.style.backgroundColor= "white";
        p.style.borderRadius="10px";
        p.style.margin="10px";
        p.style.paddingBottom="15px";
        p.style.paddingLeft="15px";
        container.appendChild(p)
        

        const taskCount = document.getElementById("taskAssigned").innerText;
        const convertTaskCount = parseInt(taskCount);
        const taskCount2= convertTaskCount-1;
        document.getElementById("taskAssigned").innerText=taskCount2;

        const navNumber = document.getElementById("navNumber").innerText;
        const convertNavNumber = parseInt(navNumber);
        const navNumber2= convertNavNumber+1;
        document.getElementById("navNumber").innerText=navNumber2;

        this.disabled = true;

})
